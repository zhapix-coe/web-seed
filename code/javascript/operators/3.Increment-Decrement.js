// Increment / Decrement Operators

let count = 10;
console.log('IntialCount ---->',count);
console.log('---------------------------------------------------------');

// Using Addition Assignment operator
count+=1;
console.log('Increment using Addition Assignment --->',count);
console.log('---------------------------------------------------------');


// Using Increment Operator
count++;
console.log('Using count++ --->',count);
console.log('---------------------------------------------------------');

// Decrement Operator
count--;
console.log('Using count-- -->',count);
console.log('---------------------------------------------------------');
