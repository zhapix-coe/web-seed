// To change case of a string
let enrollmentDescription= 'Santhosh has enrolled in an Internship Program';

//1. To Change to uppercase using toUpperCase()
let enrollmentUpper = enrollmentDescription.toUpperCase();
console.log('***************************************************************************');
console.log('Details ---->',enrollmentDescription);
console.log('----------------------------------------------------------------------------');
console.log('Details in UpperCase ---->',enrollmentUpper);
console.log('***************************************************************************');

//1. To Change to lowercase using toLowerCase()
let enrollmentToLower = enrollmentDescription.toLowerCase();
console.log('***************************************************************************');
console.log('Details ---->',enrollmentDescription);
console.log('----------------------------------------------------------------------------');
console.log('Details in LowerCase ---->',enrollmentToLower);
console.log('***************************************************************************');
