// Assignment Operator (=)
// Assigning value 10 to variable a
let a = 10;
console.log('Initial Value of a -->', a);
console.log('---------------------------------------------------------');

// Addition Assignment - Approach 1
a = a + 20;
console.log('Addition Assignment(Approach 1) a=a+20-->', a);
console.log('---------------------------------------------------------');

// Addition Assignment - Approach 2
a += 10;
console.log('Addition Assignment(Approach 2) a+=10-->', a);
console.log('---------------------------------------------------------');

// Subtraction Assignment - Approach 1
a = a - 20;
console.log('Subtraction Assignment(Approach 1) a=a-20-->', a);
console.log('---------------------------------------------------------');

// Subtraction Assignment - Approach 2
a -= 10;
console.log('Subtraction Assignment(Approach 2) a-=10-->', a);
console.log('---------------------------------------------------------');
