let enrollmentDetails='Santhosh has enrolled in an Internship Program';

// Get first Letter of a String using charAt(index)
let firstLetter = enrollmentDetails.charAt(0);
console.log('First letter of program using charAt(index) --->',firstLetter);
console.log('***************************************************************************');


// Get first letter of a string using String[index]
let firstLetter1 = enrollmentDetails[0];
console.log('First letter of program using String[index] --->',firstLetter1);

