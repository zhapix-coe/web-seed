// Comparison Operator
let appleCount = 10;
let bananaCount = 20;
let orangeCount = 30;
console.log('---------------------------------------------------------');

//Equals Operator
let isBothEqual = (appleCount == bananaCount);
console.log('Is both apple and banana are equal? -->', isBothEqual);
console.log('---------------------------------------------------------');

// Greater than Operator
let isOrangeGreater = (orangeCount > appleCount)
console.log('Is Orange in more count? --->', isOrangeGreater);
console.log('---------------------------------------------------------');

let grapeCount = '10';

// Compare Values only
let isGraphEqualtoApple = (grapeCount == appleCount);
console.log('Is the grape count equal to the apple count? --->', isGraphEqualtoApple);
console.log('---------------------------------------------------------');

// Comapare both Values and type
let isSameType = (grapeCount === appleCount);
console.log('Are Apple and Grape the same type?', isSameType);
console.log('---------------------------------------------------------');
